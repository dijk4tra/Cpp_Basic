#include <string.h>
#include <iostream>

using std::cout;
using std::endl;

/**
 * 左值与右值：
 *
 * 1. 左值 lvalue
 *    一般可以理解为：
 *    有名字、可以取地址、生命周期相对稳定的值。
 *
 *    常见左值：
 *    - 普通变量
 *    - 已经命名的对象
 *    - 字符串字面值
 *
 *    例如：
 *    int a = 1;
 *    &a;          // 可以取地址，a 是左值
 *
 *
 * 2. 右值 rvalue
 *    一般可以理解为：
 *    没有名字、不能取地址、生命周期较短的临时值。
 *
 *    常见右值：
 *    - 字面值常量
 *    - 表达式计算结果
 *    - 临时对象
 *    - 匿名对象
 *
 *    例如：
 *    100;
 *    a + b;
 *    Point{};
 *
 *
 * 3. 引用分类
 *
 *    引用可以分为：
 *
 *    1）左值引用
 *       - 非 const 左值引用
 *       - const 左值引用
 *
 *       非 const 左值引用：
 *       只能绑定左值，不能绑定右值。
 *
 *       const 左值引用：
 *       既可以绑定左值，也可以绑定右值。
 *
 *
 *    2）右值引用
 *
 *       语法：
 *       数据类型 && 引用名 = 右值;
 *
 *       右值引用只能绑定右值，
 *       它是 C++11 引入移动语义的重要基础。
 *
 *
 * 4. 为什么需要右值引用？
 *
 *    const 左值引用虽然可以绑定右值，
 *    但是它无法区分传入的是左值还是右值。
 *
 *    对于资源管理类来说，
 *    如果传入的是一个即将销毁的临时对象，
 *    我们希望可以直接“拿走”它的资源，
 *    而不是重新申请空间再复制内容。
 *
 *    右值引用正是为了识别这种临时对象，
 *    从而支持后续的移动构造和移动赋值。
 */

class String
{
public:
    /**
     * 默认构造函数
     *
     * 创建一个空字符串对象。
     * m_pstr 指向一块只存放 '\0' 的堆空间。
     */
    String()
    /* : _pstr(nullptr) */
    : m_pstr(new char[1]())
    {
        cout << "String()" << endl;
    }

    /**
     * 有参构造函数
     *
     * 根据 C 风格字符串 pstr 创建 String 对象。
     * 这里需要申请 strlen(pstr) + 1 个字符空间，
     * 多出来的 1 个字符用来存放字符串结束符 '\0'。
     */
    String(const char *pstr)
    : m_pstr(new char[strlen(pstr) + 1]())
    {
        cout << "String(const char *)" << endl;
        strcpy(m_pstr, pstr);
    }

    /**
     * 拷贝构造函数
     *
     * 使用一个已经存在的 String 对象初始化新对象。
     * 因为类中管理堆资源，所以这里必须进行深拷贝。
     */
    String(const String & rhs)
    : m_pstr(new char[strlen(rhs.m_pstr) + 1]())
    {
        cout << "String(const String &)" << endl;
        strcpy(m_pstr, rhs.m_pstr);
    }

    /**
     * 拷贝赋值运算符函数
     *
     * 用一个已经存在的 String 对象给当前对象赋值。
     *
     * 注意：
     * 赋值运算符函数中需要处理：
     * 1. 自赋值
     * 2. 释放旧资源
     * 3. 申请新资源
     * 4. 深拷贝数据
     * 5. 返回 *this
     */
    String & operator=(const String & rhs)
    {
        cout << "String &operator=(const String &)" << endl;

        // 防止自赋值
        if(this != &rhs)
        {
            // 释放当前对象原来管理的堆空间
            if(m_pstr)
            {
                delete [] m_pstr;
            }

            // 重新申请堆空间并复制 rhs 中的字符串内容
            m_pstr = new char[strlen(rhs.m_pstr) + 1]();
            strcpy(m_pstr, rhs.m_pstr);
        }

        return *this;
    }

    /**
     * 获取字符串长度
     *
     * const 成员函数表示该函数不会修改当前对象。
     */
    size_t length() const
    {
        size_t len = 0;

        if(m_pstr)
        {
            len = strlen(m_pstr);
        }

        return len;
    }

    /**
     * 返回 C 风格字符串
     *
     * 返回 const char *，
     * 表示外部可以读取字符串内容，
     * 但不应该通过返回的指针修改内容。
     */
    const char * c_str() const
    {
        if(m_pstr)
        {
            return m_pstr;
        }
        else
        {
            return nullptr;
        }
    }

    /**
     * 析构函数
     *
     * 对象生命周期结束时自动调用，
     * 用来释放 m_pstr 指向的堆空间。
     */
    ~String()
    {
        cout << "~String()" << endl;

        if(m_pstr)
        {
            delete [] m_pstr;
            m_pstr = nullptr;
        }
    }

    // 打印字符串内容
    void print() const
    {
        if(m_pstr)
        {
            cout << "m_pstr = " << m_pstr << endl;
        }
        else
        {
            cout << endl;
        }
    }

private:
    // 指向堆空间的字符指针
    // String 对象负责管理这块资源
    char * m_pstr;
};

// 空类，用来演示对象、临时对象的左值和右值属性
class Point{};

void test1()
{
    /**
     * 常见的左值
     *
     * 左值一般具有以下特点：
     * 1. 有名字
     * 2. 可以取地址
     * 3. 生命周期通常不只存在于当前表达式中
     */

    int a = 1;
    int b = 2;

    // a 是有名字的变量，可以取地址，所以 a 是左值
    &a;

    // b 也是有名字的变量，可以取地址，所以 b 是左值
    &b;

    // pt 是有名字的对象，可以取地址，所以 pt 是左值
    Point pt;
    &pt;

    /**
     * 字符串字面值比较特殊。
     *
     * "hello" 是字符串常量，
     * 它存放在静态存储区，
     * 生命周期贯穿整个程序运行期间。
     *
     * 因此字符串字面值可以取地址，
     * 所以它属于左值。
     */
    &"hello";


    /**
     * 常见的右值
     *
     * 右值一般具有以下特点：
     * 1. 没有名字
     * 2. 通常不能取地址
     * 3. 生命周期较短，常常是临时值
     */

    /**
     * a + b 是表达式计算结果。
     *
     * 这个结果是一个临时值，
     * 没有名字，不能取地址，
     * 所以是右值。
     */
    /* &(a + b); */

    /**
     * a++ 是后置自增表达式。
     *
     * 后置自增的含义是：
     * 先产生 a 原来的值作为表达式结果，
     * 然后再让 a 自增。
     *
     * 表达式 a++ 的结果是一个临时值，
     * 所以不能取地址，是右值。
     *
     * 注意：
     * ++a 前置自增返回的是 a 本身，
     * 所以 ++a 是左值，可以取地址。
     */
    /* &a++; */

    /**
     * Point{} 会创建一个匿名临时对象。
     *
     * 这个对象没有名字，
     * 是一个临时对象，
     * 所以是右值，不能直接取地址。
     */
    /* &Point{}; */

    /**
     * 100 是字面值常量。
     *
     * 它没有名字，
     * 也不能取地址，
     * 所以是右值。
     */
    /* &100; */
}

void test2()
{
    int a = 1;

    /**
     * 非 const 左值引用
     *
     * 语法：
     * 数据类型 & 引用名 = 左值;
     *
     * 非 const 左值引用只能绑定左值，
     * 不能绑定右值。
     */
    int & ref = a;

    /**
     * ref 是 a 的引用。
     *
     * 修改 ref，
     * 本质上就是修改 a。
     */
    ref = 2;

    /**
     * 1 是字面值常量，是右值。
     *
     * 非 const 左值引用不能绑定右值，
     * 所以下面这行代码是错误的。
     */
    /* int & ref2 = 1; */


    /**
     * const 左值引用
     *
     * 语法：
     * const 数据类型 & 引用名 = 左值或右值;
     *
     * const 左值引用既可以绑定左值，
     * 也可以绑定右值。
     */

    // a 是左值，const 左值引用可以绑定左值
    const int & ref3 = a;

    /**
     * ref3 是 const 引用，
     * 不能通过 ref3 修改绑定的对象。
     */
    /* ref3 = 2; */

    /**
     * 1 是右值。
     *
     * const 左值引用可以绑定右值。
     *
     * 当 const 引用绑定右值时，
     * 右值的生命周期会被延长到引用的生命周期结束。
     */
    const int & ref4 = 1;


    /**
     * const 左值引用的问题：
     *
     * const int & 既可以绑定左值，
     * 也可以绑定右值。
     *
     * 这虽然方便，
     * 但是它无法区分传入的到底是左值还是右值。
     *
     * 在移动语义中，
     * 我们需要识别“临时对象”这种右值，
     * 因为临时对象马上就要销毁，
     * 可以考虑直接转移它内部的资源。
     *
     * 因此 C++11 引入了右值引用。
     */


    /**
     * 右值引用
     *
     * 语法：
     * 数据类型 && 引用名 = 右值;
     *
     * 右值引用只能绑定右值，
     * 不能直接绑定左值。
     */

    /**
     * a 是有名字的变量，是左值。
     *
     * 右值引用不能绑定左值，
     * 所以下面这行代码是错误的。
     */
    /* int && ref5 = a; */

    /**
     * 1 是字面值常量，是右值。
     *
     * 右值引用可以绑定右值。
     */
    int && ref6 = 1;

    /**
     * 注意：
     *
     * 虽然 ref6 的类型是 int &&，
     * 但是 ref6 本身是有名字的变量。
     *
     * 只要一个表达式有名字，
     * 它在表达式中就是左值。
     *
     * 所以后续如果直接使用 ref6，
     * ref6 表达式本身是左值。
     *
     * 这一点在学习 std::move 时非常重要：
     * std::move 的作用就是把一个左值强制转换成右值引用类型。
     */
}

int main(int argc, char * argv[])
{
    test1();

    // 如果想观察引用相关代码，也可以调用 test2()
    // test2();

    return 0;
}