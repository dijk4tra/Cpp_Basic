#include <iostream>
#include <functional>

using namespace std;


/**
 * 引用折叠 reference collapsing
 *
 * 在 C++ 中，引用本身不能直接再引用。
 *
 * 例如：
 *
 * int & & ref;     // 不能直接这样写
 * int && & ref;    // 也不能直接这样写
 *
 * 但是在模板类型推导、typedef、using、decltype 等场景中，
 * 编译器可能会间接形成“引用的引用”。
 *
 * 这时 C++ 会按照固定规则进行引用折叠。
 *
 *
 * 引用折叠规则：
 *
 * T &  &   -> T &
 * T && &   -> T &
 * T &  &&  -> T &
 * T && &&  -> T &&
 *
 *
 * 规则总结：
 *
 * 只要有左值引用 & 参与，
 * 最终结果就是左值引用 &。
 *
 * 只有两个都是右值引用 && 时，
 * 最终结果才是右值引用 &&。
 *
 *
 * 简单记忆：
 *
 * & 具有更强的“传染性”。
 *
 * 只要出现 &，
 * 最终就是 &。
 */


/**
 * 左值和右值
 *
 * 左值：
 * 有名字、可以取地址、表达式结束后仍然存在的对象。
 *
 * 例如：
 *
 * int num = 1;
 *
 * num 是左值。
 *
 *
 * 右值：
 * 临时值、字面量、表达式结束后通常就不再存在的值。
 *
 * 例如：
 *
 * 1
 * num + 1
 *
 * 这些通常是右值。
 *
 *
 * 注意：
 * 判断左值和右值，
 * 不是看它在等号左边还是右边，
 * 而是看它是否有稳定的身份、是否可以取地址。
 */


/**
 * 万能引用 universal reference / forwarding reference
 *
 * 模板中出现下面这种形式：
 *
 * template<typename T>
 * void func(T && t)
 *
 * 当满足两个条件时：
 *
 * 1. T 是需要推导的模板类型参数
 * 2. 形参形式正好是 T&&
 *
 * 那么这里的 T&& 不是普通右值引用，
 * 而是万能引用，也叫转发引用。
 *
 *
 * 万能引用的特点：
 *
 * 既可以接收左值，
 * 也可以接收右值。
 *
 *
 * 当传入左值时：
 *
 * int num = 1;
 * func(num);
 *
 * T 会被推导为：
 *
 * int&
 *
 * 此时形参类型是：
 *
 * T&&
 *
 * 代入后：
 *
 * int& &&
 *
 * 根据引用折叠规则：
 *
 * int& &&  -> int&
 *
 * 所以最终形参类型是左值引用。
 *
 *
 * 当传入右值时：
 *
 * func(1);
 *
 * T 会被推导为：
 *
 * int
 *
 * 此时形参类型是：
 *
 * T&&
 *
 * 代入后：
 *
 * int&&
 *
 * 所以最终形参类型是右值引用。
 *
 *
 * 这就是为什么同一个 T&& 形参，
 * 既可以接收左值，
 * 也可以接收右值。
 */


/**
 * 普通左值引用形参
 *
 * template<typename T>
 * void func(T & t)
 *
 * 这里的 T& 是普通左值引用形参。
 *
 * 它只能接收左值，
 * 不能接收右值。
 *
 *
 * 例如：
 *
 * int num = 1;
 * func(num);   // 正确
 * func(1);     // 错误
 *
 *
 * 原因：
 * 非 const 左值引用不能绑定到右值。
 */


/**
 * func
 *
 * 函数模板。
 *
 * 形参类型是：
 *
 * T&
 *
 * 这是普通左值引用形参。
 *
 * 特点：
 * 只能接收左值。
 */
template<typename T>
void func(T & t)
{
    cout << "func(T & t)" << endl;
}


/**
 * func2
 *
 * 函数模板。
 *
 * 形参类型是：
 *
 * T&&
 *
 * 因为 T 是模板参数，并且形参正好写成 T&&，
 * 所以这里的 T&& 是万能引用。
 *
 * 特点：
 * 既可以接收左值，
 * 也可以接收右值。
 */
template<typename T>
void func2(T && t)
{
    cout << "func2(T && t)" << endl;
}


void test1()
{
    /**
     * num 是一个有名字的变量。
     *
     * 有名字的变量是左值。
     */
    int num = 1;

    /**
     * 调用 func(num)
     *
     * func 的形参是 T&。
     *
     * num 是左值，
     * 所以可以绑定到普通左值引用。
     *
     * 模板推导结果：
     *
     * T = int
     *
     * 形参类型：
     *
     * T&
     *
     * 代入后：
     *
     * int&
     */
    func(num);

    /**
     * 调用 func(1)
     *
     * 1 是右值。
     *
     * func 的形参是 T&，
     * 也就是普通非 const 左值引用。
     *
     * 非 const 左值引用不能绑定到右值，
     * 所以这行代码会编译错误。
     */
    // func(1);

    cout << "-------" << endl;

    /**
     * func2 的形参是 T&&。
     *
     * 因为 T 是模板参数，
     * 所以这里的 T&& 是万能引用。
     */

    /**
     * 调用 func2(num)
     *
     * num 是左值。
     *
     * 万能引用接收左值时：
     *
     * T 会被推导为 int&。
     *
     * 形参类型：
     *
     * T&&
     *
     * 代入后：
     *
     * int& &&
     *
     * 根据引用折叠规则：
     *
     * int& &&  -> int&
     *
     * 所以最终形参类型是：
     *
     * int&
     *
     * 因此可以成功接收左值 num。
     */
    func2(num);

    /**
     * 调用 func2(1)
     *
     * 1 是右值。
     *
     * 万能引用接收右值时：
     *
     * T 会被推导为 int。
     *
     * 形参类型：
     *
     * T&&
     *
     * 代入后：
     *
     * int&&
     *
     * 所以最终形参类型是：
     *
     * int&&
     *
     * 因此可以成功接收右值 1。
     */
    func2(1);
}


int main()
{
    // 调用测试函数
    test1();

    return 0;
}