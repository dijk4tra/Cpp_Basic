#include <iostream>

using std::cout;
using std::endl;

/*
 * 对象数据成员
 *
 * 对象数据成员指的是：
 * 一个类中包含另一个类的对象作为成员变量。
 *
 * 例如：
 * class B
 * {
 * };
 * class A
 * {
 *     B b;
 * };
 * 这里 b 就是 A 类中的对象数据成员。
 *
 *
 * 在当前代码中：
 * class C
 * {
 *     A m_a;
 *     B m_b;
 * };
 * m_a 是 A 类型的对象数据成员。
 * m_b 是 B 类型的对象数据成员。
 *
 *
 * 对象数据成员的特点：
 * 
 * 1. 创建外层对象时，成员对象会先被创建
 *    例如：
 *    C c;
 *    创建 C 对象之前，
 *    C 中的成员对象 m_a 和 m_b 必须先构造完成。
 *
 * 
 * 2. 对象成员最好在构造函数初始化列表中初始化
 *    例如：
 *    C(int a, int b)
 *    : m_a(a),
 *      m_b(b)
 *    {
 *    }
 *    这里 m_a(a) 会调用 A(int)，
 *    m_b(b) 会调用 B(int)。
 *
 * 
 * 3. 如果构造函数初始化列表中没有显式初始化对象成员
 *    编译器会尝试调用该对象成员的无参构造函数。
 *    例如：
 *    C()
 *    {
 *    }
 *    虽然没有写初始化列表，
 *    但是 m_a 会自动调用 A()，
 *    m_b 会自动调用 B()。
 *
 * 
 * 4. 如果对象数据成员没有无参构造函数
 *    那么外层类的构造函数就必须在初始化列表中
 *    显式调用它的有参构造函数。
 *    例如：
 *    class A
 *    {
 *    public:
 *        A(int data) {}
 *    };
 *
 *    class C
 *    {
 *    public:
 *        C()
 *        : m_a(10)
 *        {
 *        }
 *
 *        A m_a;
 *    };
 *
 * 
 * 5. 初始化顺序
 *    对象数据成员的初始化顺序，
 *    只和它们在类中声明的顺序有关，
 *    和初始化列表中的书写顺序无关。
 *
 *    当前 C 类中：
 *    A m_a;
 *    B m_b;
 *
 *    所以一定是：
 *    先构造 m_a
 *    再构造 m_b
 *    最后执行 C 构造函数体
 *
 *
 * 6. 析构顺序
 *    析构顺序和构造顺序相反。
 *
 *    当前 C 类中：
 *    构造顺序：
 *    A()
 *    B()
 *    C()
 *
 *    析构顺序：
 *    ~C()
 *    ~B()
 *    ~A()
 */

class A
{
public:
    // A 的无参构造函数
    // 当创建 A 对象，并且没有传入参数时调用。
    // 例如：
    // A a;
    A()
    {
        cout << "A()" << endl;
    }

    // A 的有参构造函数
    // 当使用一个 int 创建 A 对象时调用。
    // 例如：
    // A a{10};
    A(int data)
    : m_dataA(data)
    {
        cout << "A(int)" << endl;
    }

    // A 的析构函数
    // 当 A 对象销毁时自动调用。
    ~A()
    {
        cout << "~A()" << endl;
    }

    // A 类中的普通数据成员
    int m_dataA;
};

class B
{
public:
    // B 的无参构造函数
    // 当创建 B 对象，并且没有传入参数时调用。
    B()
    {
        cout << "B()" << endl;
    }

    // B 的有参构造函数
    // 当使用一个 int 创建 B 对象时调用。
    B(int data)
    : m_dataB(data)
    {
        cout << "B(int)" << endl;
    }

    // B 的析构函数
    // 当 B 对象销毁时自动调用。
    ~B()
    {
        cout << "~B()" << endl;
    }

    // B 类中的普通数据成员
    int m_dataB;
};

class C
{
public:
    // C 的无参构造函数
    // 这里没有写初始化列表：
    // C()
    // {
    // }
    //
    // 但是 C 类中有两个对象数据成员：
    // A m_a;
    // B m_b;
    //
    // 所以在执行 C 构造函数体之前，
    // 编译器会先隐式调用对象成员的无参构造函数：
    // 1. m_a 调用 A()
    // 2. m_b 调用 B()
    // 3. 再执行 C() 构造函数体
    //
    // 因此创建 C c; 时输出顺序通常是：
    // A()
    // B()
    // C()
    C()
    {
        cout << "C()" << endl;
    }

    // C 的有参构造函数
    // 创建 C 对象时传入两个 int：
    // C c1{1, 2};
    // 会调用这个构造函数。
    C(int a, int b)
    : m_a(a)
    , m_b(b)
    {
        /*
         * 初始化列表：
         * m_a(a)
         * 表示用参数 a 初始化成员对象 m_a。
         * 因为 m_a 是 A 类型，
         * 所以这里会调用 A(int)。
         *
         * m_b(b)
         * 表示用参数 b 初始化成员对象 m_b。
         * 因为 m_b 是 B 类型，
         * 所以这里会调用 B(int)。
         *
         *
         * 执行顺序：
         * 1. 先构造 m_a，调用 A(int)
         * 2. 再构造 m_b，调用 B(int)
         * 3. 最后执行 C(int, int) 构造函数体
         *
         * 所以创建：
         * C c1{1, 2};
         * 输出顺序通常是：
         * A(int)
         * B(int)
         * C(int,int)
         */
        cout << "C(int,int)" << endl;
    }

    // C 的析构函数
    // 当 C 对象销毁时自动调用。
    //
    // 注意：
    // 析构时会先执行外层对象 C 的析构函数体，
    // 然后再按照成员构造的相反顺序销毁成员对象。
    //
    // 对于当前类：
    // 构造顺序：
    // A m_a
    // B m_b
    // C 构造函数体
    //
    // 析构顺序：
    // C 析构函数体
    // B m_b 析构
    // A m_a 析构
    //
    // 所以销毁 C c1{1, 2}; 时输出通常是：
    // ~C()
    // ~B()
    // ~A()
    ~C()
    {
        cout << "~C()" << endl;
    }

    // 对象类型的数据成员
    // m_a 是 A 类型的对象成员。
    // 创建 C 对象时，m_a 会先被构造。
    A m_a;

    // m_b 是 B 类型的对象成员。
    // 创建 C 对象时，m_b 会在 m_a 之后被构造。
    B m_b;
};

void test1()
{
    /*
     * 如果取消下面这行注释：
     * C c;
     * 会调用 C 的无参构造函数。
     *
     * 因为 C() 没有初始化列表，
     * 所以 m_a 和 m_b 会调用各自的无参构造函数。
     *
     * 输出顺序通常是：
     * A()
     * B()
     * C()
     *
     * test1 结束时析构：
     * ~C()
     * ~B()
     * ~A()
     */
    /* C c; */

    /*
     * 创建 C 类型对象 c1。
     *
     * 传入参数 1 和 2，
     * 调用 C(int, int)。
     *
     * 初始化列表：
     * m_a(a)
     * 调用 A(int)，m_a.m_dataA 初始化为 1。
     * m_b(b)
     * 调用 B(int)，m_b.m_dataB 初始化为 2。
     * 然后执行 C(int, int) 构造函数体。
     *
     * 输出顺序通常是：
     * A(int)
     * B(int)
     * C(int,int)
     *
     * 当 test1 结束时，
     * c1 是局部对象，会自动销毁。
     *
     * 析构输出顺序通常是：
     * ~C()
     * ~B()
     * ~A()
     */
    C c1{1,2};
}

int main(int argc, char * argv[])
{
    // 调用 test1，测试对象数据成员的构造和析构顺序
    test1();

    return 0;
}