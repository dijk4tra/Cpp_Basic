#include <iostream>

using std::cout;
using std::endl;

/*
 * 特殊的数据成员：
 *
 * 在类中，有一些数据成员比较特殊。
 * 它们的初始化方式和普通成员变量不完全一样。
 *
 * 常见的特殊数据成员有：
 *
 * 1. 常量 const 数据成员
 *    例如：
 *    const int m_data2;
 *    const 成员一旦初始化之后，
 *    后续就不能再修改。
 *
 * 2. 引用数据成员
 *    例如：
 *    int & m_ref;
 *    引用必须在定义时绑定到某个对象，
 *    一旦绑定之后不能再改变引用目标。
 *
 * 3. 对象数据成员
 *    例如：
 *    Point m_pt;
 *    一个类中包含另一个类的对象。
 *    创建外层对象时，
 *    内层对象也要先被构造。
 *
 * 4. 静态数据成员
 *    例如：
 *    static int m_count;
 *    static 成员属于类本身，
 *    不属于某一个具体对象。
 *
 *
 * const 数据成员的初始化：
 * const 数据成员必须进行初始化。
 * 因为 const 成员一旦创建完成，
 * 就不能再被赋值修改。
 *
 * 所以不能在构造函数体中写：
 * m_data2 = 10;
 * 这不是初始化，而是赋值。
 * 对 const 成员来说，赋值是不允许的。
 *
 * 正确做法：
 * const 数据成员应该在构造函数初始化列表中初始化：
 * MyClass()
 * : m_data2(10)
 * {
 * }
 *
 *
 * 如果类中有多个构造函数：
 * 每一个构造函数都要保证 const 成员能够被初始化。
 * 可以有两种方式：
 * 1. 每个构造函数都在初始化列表中显式初始化 const 成员
 * 2. C++11 之后，在类内给 const 成员提供默认值
 *    const int m_data2 = 20;
 *    如果某个构造函数没有在初始化列表中初始化它，
 *    就会使用类内默认值 20。
 *
 *
 * 注意：
 * 如果构造函数初始化列表中显式初始化了 const 成员，
 * 那么初始化列表中的值优先。
 *
 * 如果构造函数没有显式初始化，
 * 才会使用类内默认值。
 */

class MyClass
{
public:
    // 无参构造函数
    // 创建对象：
    // MyClass obj;
    // 会调用这个构造函数。
    MyClass()
    : m_data2(10)
    {
        /*
         * 这里使用初始化列表初始化 m_data2。
         * 虽然 m_data2 在类内写了默认值 20：
         * const int m_data2 = 20;
         *
         * 但是当前构造函数的初始化列表中写了：
         * m_data2(10)
         *
         * 所以最终 m_data2 的值是 10，
         * 而不是 20。
         *
         *
         * 注意：
         * 不能在构造函数体中写：
         * m_data2 = 10;
         *
         * 因为构造函数体内部执行时，
         * m_data2 已经初始化完成了。
         *
         * 对 const 成员来说，
         * 初始化完成之后就不能再赋值。
         */
    }

    // 有参构造函数
    // 创建对象：
    // MyClass obj{100};
    // 会调用这个构造函数。
    MyClass(int data1)
    : m_data1(data1)
    , m_data2(100)
    {
        /*
         * m_data1(data1)
         * 使用形参 data1 初始化普通成员 m_data1。
         *
         * m_data2(100)
         * 使用初始化列表初始化 const 成员 m_data2。
         * 因为初始化列表中显式写了 m_data2(100)，
         * 所以类内默认值 20 不会生效。
         *
         * 最终：
         * m_data2 == 100
         */
    }

    // 普通数据成员
    // int 类型的普通成员变量。
    //
    // 如果构造函数没有初始化它，
    // 并且类内也没有给默认值，
    // 那么它的值是不确定的。
    //
    // 例如：
    // MyClass obj;
    //
    // 当前无参构造函数只初始化了 m_data2，
    // 没有初始化 m_data1。
    // 所以 obj.m_data1 的值是不确定的。
    int m_data1;

    // 常量数据成员
    // const 表示该成员变量一旦初始化之后，
    // 就不能再被修改。
    //
    // C++11 之后，
    // 可以在类内直接给非静态数据成员提供默认值。
    //
    // 这里的 20 是 m_data2 的类内默认值。
    //
    // 如果某个构造函数没有在初始化列表中显式初始化 m_data2，
    // 那么 m_data2 会使用这个默认值 20。
    //
    // 如果构造函数初始化列表中显式初始化了 m_data2，
    // 那么以初始化列表中的值为准。
    const int m_data2 = 20;
};

void test1()
{
    // const 修饰普通局部变量
    // num 是一个常量，
    // 初始化之后不能再修改。
    const int num = 1;

    // 错误：
    // const 变量不能再次赋值。
    /* num = 2; */

    cout << num << endl;


    /*
     * const 数据成员和 const 局部变量类似：
     *
     * const 局部变量：
     * const int num = 1;
     * 必须在定义时初始化。
     *
     * const 数据成员：
     * const int m_data2;
     * 必须在对象构造阶段初始化。
     *
     * 对类来说，
     * 构造阶段真正初始化成员的位置就是初始化列表。
     */
}

void test2()
{
    // 调用无参构造函数
    // m_data2 使用初始化列表中的值 10
    // m_data1 没有被初始化，
    // 它的值是不确定的。
    MyClass obj1;

    // 调用有参构造函数
    // m_data1 使用 data1 初始化，值为 50
    // m_data2 使用初始化列表中的值 100
    MyClass obj2{50};

    cout << obj1.m_data2 << endl; // 10
    cout << obj2.m_data1 << endl; // 50
    cout << obj2.m_data2 << endl; // 100

    // 错误：
    // m_data2 是 const 数据成员，
    // 对象创建完成之后不能再修改。
    /* obj2.m_data2 = 200; */
}

int main(int argc, char * argv[])
{
    // test1 演示普通 const 局部变量
    test1();

    // test2 演示 const 数据成员的初始化方式
    /* test2(); */

    return 0;
}