#include <iostream>
#include <vector>
#include <list>
#include <deque>
#include <unordered_set>

using namespace std;

/**
 * 迭代器 iterator：
 *
 * 迭代器可以理解成一种“广义指针”。
 *
 * 它的作用是：
 * 让我们可以按照统一的方式访问不同容器中的元素。
 *
 * 例如：
 * vector、list、deque、set、map 等容器的底层结构不同，
 * 但是都可以通过迭代器来遍历元素。
 *
 *
 * 迭代器常见操作：
 *
 * 1. 解引用
 *    *it
 *    表示访问迭代器 it 指向的元素。
 *
 * 2. 自增
 *    ++it
 *    表示让迭代器向后移动一个位置。
 *
 * 3. 自减
 *    --it
 *    表示让迭代器向前移动一个位置。
 *    不是所有迭代器都支持这个操作。
 *
 * 4. 随机移动
 *    it += 3
 *    it -= 3
 *    表示让迭代器一次性移动多个位置。
 *    只有随机访问迭代器才支持。
 */


/**
 * 迭代器分类：
 *
 * 1. 输出迭代器 output iterator
 *
 *    主要用于写操作。
 *
 *    例如：
 *    *it = 100;
 *    表示把 100 写入迭代器 it 指向的位置。
 *
 *
 * 2. 输入迭代器 input iterator
 *
 *    主要用于读操作。
 *
 *    例如：
 *    cout << *it << endl;
 *    表示读取迭代器 it 指向位置上的数据。
 *
 *
 * 3. 前向迭代器 forward iterator
 *
 *    可以向后移动，
 *    但是不能向前移动。
 *
 *    支持：
 *    ++it
 *
 *    不支持：
 *    --it
 *    it += n
 *
 *
 * 4. 双向迭代器 bidirectional iterator
 *
 *    可以向后移动，
 *    也可以向前移动。
 *
 *    支持：
 *    ++it
 *    --it
 *
 *    不支持：
 *    it += n
 *
 *
 * 5. 随机访问迭代器 random access iterator
 *
 *    功能最强。
 *
 *    既可以单步移动，
 *    也可以随机移动。
 *
 *    支持：
 *    ++it
 *    --it
 *    it += n
 *    it -= n
 *    it[n]
 *
 *
 * 迭代器能力从弱到强大致为：
 *
 * 输出 / 输入迭代器
 *        ↓
 * 前向迭代器
 *        ↓
 * 双向迭代器
 *        ↓
 * 随机访问迭代器
 */


/**
 * 常见容器对应的迭代器类型：
 *
 * 1. unordered_set / unordered_map
 *
 *    无序关联式容器。
 *
 *    常见情况下提供前向迭代器。
 *
 *    只能向后移动：
 *    ++it
 *
 *
 * 2. set / map / multiset / multimap
 *
 *    有序关联式容器。
 *
 *    提供双向迭代器。
 *
 *    可以向后、向前移动：
 *    ++it
 *    --it
 *
 *
 * 3. list
 *
 *    双向链表。
 *
 *    提供双向迭代器。
 *
 *    可以：
 *    ++it
 *    --it
 *
 *    不能：
 *    it += n
 *
 *
 * 4. vector / deque
 *
 *    提供随机访问迭代器。
 *
 *    可以：
 *    ++it
 *    --it
 *    it += n
 *    it -= n
 *    it[n]
 *
 *
 * 5. queue / stack
 *
 *    注意：
 *    queue 和 stack 是容器适配器，
 *    它们本身不提供迭代器。
 *
 *    所以不能直接使用 begin() / end() 遍历。
 *
 *    如果要访问元素，
 *    stack 通常使用 top() + pop()。
 *    queue 通常使用 front() + pop()。
 */


// 从容器中获取迭代器
void test1()
{
    /**
     * unordered_set：
     *
     * unordered_set 是无序关联式容器。
     *
     * 特点：
     * 1. 元素不允许重复
     * 2. 元素没有固定顺序
     * 3. 底层通常使用哈希表实现
     *
     *
     * unordered_set<int> box{1,2,3,4,5};
     *
     * 表示创建一个 unordered_set 容器，
     * 里面存放 int 类型的数据。
     *
     * 注意：
     * unordered_set 中元素的遍历顺序，
     * 不一定是插入顺序。
     */
    unordered_set<int> box{1,2,3,4,5};

    /**
     * begin：
     * 获取指向容器第一个元素的迭代器。
     *
     * auto：
     * 让编译器自动推导变量类型。
     *
     * 这里 it 的类型可以理解为：
     * unordered_set<int>::iterator
     *
     * 对 unordered_set 来说，
     * 它的迭代器通常是前向迭代器。
     */
    auto it = box.begin();

    /**
     * 前向迭代器支持 ++it。
     *
     * ++it 表示让迭代器向后移动一个位置。
     */
    ++it;
    ++it;

    /**
     * 前向迭代器不能向前移动。
     *
     * 所以不支持 --it。
     */
    // it--; // 错误，前向迭代器不能往左移动

    /**
     * *it：
     * 解引用迭代器。
     *
     * 表示访问 it 当前指向的元素。
     *
     * 注意：
     * unordered_set 是无序容器，
     * 所以这里输出的值不一定固定。
     */
    cout << *it << endl;
}


// list : 双向迭代器
void test2()
{
    /**
     * list：
     *
     * list 是双向链表。
     *
     * 特点：
     * 1. 插入、删除效率较高
     * 2. 不支持下标随机访问
     * 3. 迭代器是双向迭代器
     *
     *
     * list<int> box{1,2,3,4,5};
     *
     * 表示创建一个 list 容器，
     * 里面存放 1、2、3、4、5。
     */
    list<int> box{1,2,3,4,5};

    /**
     * 获取指向 list 第一个元素的迭代器。
     *
     * it 初始指向元素 1。
     */
    auto it = box.begin();

    /**
     * list 的迭代器是双向迭代器。
     *
     * 支持：
     * ++it
     * --it
     *
     * 不支持：
     * it += n
     */
    ++it;  // it 从 1 移动到 2
    ++it;  // it 从 2 移动到 3
    --it;  // it 从 3 移动回 2

    /**
     * *it = 100;
     *
     * 表示把 it 当前指向的元素修改为 100。
     *
     * 这里 it 指向的是原来的元素 2，
     * 所以执行之后，list 中的 2 被改成 100。
     */
    *it = 100; // 写操作

    /**
     * 输出 it 当前指向的元素。
     *
     * 这里输出的是 100。
     */
    cout << *it << endl;

    /**
     * list 的迭代器不是随机访问迭代器。
     *
     * 所以不能使用：
     * it += 3;
     *
     * 如果想移动 3 次，
     * 只能连续执行 3 次 ++it。
     */
    // it += 3; // 错误，双向迭代器不能随机访问，只能单步移动
}


// vector : 随机访问迭代器
void test3()
{
    /**
     * vector：
     *
     * vector 是动态数组。
     *
     * 特点：
     * 1. 内存空间连续
     * 2. 支持下标访问
     * 3. 支持随机访问迭代器
     *
     *
     * vector<int> box{1,2,3,4,5};
     *
     * 表示创建一个 vector 容器，
     * 里面存放 1、2、3、4、5。
     */
    vector<int> box{1,2,3,4,5};

    /**
     * 获取指向 vector 第一个元素的迭代器。
     *
     * it 初始指向元素 1。
     */
    auto it = box.begin();

    /**
     * vector 的迭代器是随机访问迭代器。
     *
     * 随机访问迭代器支持：
     * ++it
     * --it
     * it += n
     * it -= n
     * it[n]
     *
     * 所以它的功能比 list 的双向迭代器更强。
     */
    ++it;  // it 从 1 移动到 2
    ++it;  // it 从 2 移动到 3
    --it;  // it 从 3 移动回 2

    /**
     * 修改当前迭代器指向的元素。
     *
     * 当前 it 指向原来的元素 2，
     * 所以这里把 2 修改为 100。
     */
    *it = 100; // 写操作

    /**
     * 读取当前迭代器指向的元素。
     *
     * 这里输出 100。
     */
    cout << *it << endl; // 读操作

    /**
     * it += 3;
     *
     * 表示迭代器向后移动 3 个位置。
     *
     * 因为 vector 的迭代器是随机访问迭代器，
     * 所以可以这样直接跳跃式移动。
     *
     * 当前 it 指向修改后的 100，
     * 也就是原来第 2 个元素的位置。
     *
     * 向后移动 3 个位置后，
     * it 指向元素 5。
     */
    it += 3; // 可以随机访问

    /**
     * 输出 it 当前指向的元素。
     *
     * 这里输出 5。
     */
    cout << *it << endl;
}


