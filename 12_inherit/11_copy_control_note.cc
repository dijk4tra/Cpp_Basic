#include <iostream>

using std::cout;
using std::endl;


/**
 * 子类对象的复制控制操作：
 *
 * 1. 拷贝构造函数
 * 2. 赋值运算符函数
 *
 *
 * 子类对象进行复制时，
 * 不仅要复制子类自己定义的成员，
 * 也要复制从父类继承来的成员。
 *
 *
 * 如果子类中没有自己定义拷贝构造函数，
 * 编译器会自动生成一个默认拷贝构造函数。
 *
 * 默认拷贝构造函数会完成两部分复制：
 *
 * 1. 调用父类的拷贝构造函数，
 *    复制父类部分。
 *
 * 2. 调用子类成员自己的拷贝构造，
 *    复制子类自己定义的成员。
 *
 *
 * 也就是说：
 *
 * Son s2 = s1;
 *
 * 这句代码会调用 Son 的拷贝构造函数。
 *
 * 因为 Son 中没有显式定义拷贝构造函数，
 * 所以调用的是编译器自动生成的默认拷贝构造函数。
 *
 * 默认拷贝构造函数会把：
 * s1 中的 Father 部分 m_f
 * 和 Son 自己的部分 m_s
 * 都复制给 s2。
 */

class Father
{
public:
    // Father 的有参构造函数
    Father(int f)
    : m_f(f)
    {
        /**
         * 使用初始化列表，
         * 用参数 f 初始化成员变量 m_f。
         */
    }

    // 父类中的成员变量
    int m_f;
};


class Son : public Father
{
public:
    // Son 的有参构造函数
    Son(int f, int s)
    : Father(f) // 先调用父类 Father 的有参构造函数，初始化父类部分
    , m_s(s)    // 再初始化子类自己的成员 m_s
    {
        /**
         * 创建 Son 对象时，
         * 对象中包含两部分：
         *
         * 1. 从 Father 继承来的父类部分
         * 2. Son 自己定义的子类部分
         *
         * 所以构造 Son 对象时，
         * 需要先构造 Father 部分，
         * 再构造 Son 自己的成员。
         */
    }

    // 子类自己定义的成员变量
    int m_s;
};

void test1()
{
    // 创建 Son 对象 s1
    Son s1{1, 2};

    /**
     * s1 的父类部分 m_f 被初始化为 1。
     * s1 的子类部分 m_s 被初始化为 2。
     */

    cout << s1.m_f << endl;
    cout << s1.m_s << endl;

    cout << "-----" << endl;

    // 对象复制
    // 使用一个已经存在的对象 s1 初始化新对象 s2
    // 这里会调用 Son 的拷贝构造函数
    Son s2 = s1;

    /**
     * Son 类中没有显式定义拷贝构造函数。
     *
     * 所以编译器会自动生成一个默认拷贝构造函数。
     *
     * 默认拷贝构造函数会复制：
     *
     * 1. 父类 Father 部分
     *    也就是 m_f。
     *
     * 2. 子类 Son 自己的部分
     *    也就是 m_s。
     *
     * 因此 s2 的 m_f 和 m_s
     * 会分别得到 s1 中对应成员的值。
     */

    cout << s2.m_f << endl;
    cout << s2.m_s << endl;
}

int main(int argc, char *argv[])
{
    // 测试子类对象的拷贝构造
    test1();

    return 0;
}