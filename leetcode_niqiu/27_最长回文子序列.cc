#include <iostream>
#include <vector>
#include <string>
#include <algorithm>

using namespace std;

// 方法一: 动态规划 (二维数组,时间复杂度O(n^2),空间复杂度O(n^2))
class Solution {
public:
    int longestPalindromeSubseq(string s) {
        int n = s.length();
        // 创建一个 nxn 的二维数组，初始化为 0
        // dp[i][j] 表示字符串 s 在区间 [i, j] 内的最长回文子序列的长度
        // 状态转移会用到 dp[i+1][j-1]，所以初始化为 0 也是为了处理无效区间（如 i > j 时）
        vector<vector<int>> dp(n, vector<int>(n, 0));

        // 外层循环：从后往前遍历 i
        // 原因：计算 dp[i][j] 需要用到下一行 dp[i+1][j-1] 和 dp[i+1][j] 的数据，
        // 也就是说，大索引的状态决定小索引的状态，因此 i 必须逆序遍历。
        for (int i = n - 1; i >= 0; --i) {
            // 基础情况：当 i == j 时，区间内只有一个字符
            // 任何单个字符本身就是一个长度为 1 的回文子序列（即矩阵的主对角线）
            dp[i][i] = 1;

            // 内层循环：从 i+1 开始往后遍历 j
            // 因为 j 必须大于等于 i 才有意义（区间右端点在左端点右侧）
            for (int j = i + 1; j < n; ++j) {
                if (s[i] == s[j]) {
                    // 情况一：两端字符相同
                    // 说明这两个字符可以加入到内部区间 [i+1, j-1] 的回文序列两端，长度直接 +2
                    dp[i][j] = dp[i + 1][j - 1] + 2;
                } else {
                    // 情况二：两端字符不同
                    // 说明 s[i] 和 s[j] 无法同时出现在当前区间的最长回文序列中。
                    // 此时要么不考虑 s[i]（看区间 [i+1, j]），要么不考虑 s[j]（看区间 [i, j-1]），两者取最大值
                    dp[i][j] = max(dp[i + 1][j], dp[i][j - 1]);
                }
            }
        }

        // 最终结果：整个字符串 s 在区间 [0, n-1] 内的最长回文子序列长度
        return dp[0][n - 1];
    }
};

// 方法二: 动态规划 + 滚动数组（Rolling Array）(一维数组,时间复杂度O(n^2),空间复杂度O(n))
class Solution2 {
public:
    int longestPalindromeSubseq(std::string s) {
        int n = s.length();

        // dp[j] 表示在当前行 i 的情况下，区间 [i, j] 内的最长回文子序列长度。
        // 由于空间压缩，我们只维护一维数组，它通过在循环中不断被覆盖来模拟二维的行更新。
        std::vector<int> dp(n, 0);

        // 外层循环：从后往前遍历 i（作为区间的左端点）
        for (int i = n - 1; i >= 0; --i) {

            // 基础情况：当区间只有字符 s[i] 自己时（即 j == i），长度为 1
            // 这相当于原二维矩阵主对角线上的 dp[i][i] = 1
            dp[i] = 1;

            // prev 核心变量：用来暂存原二维矩阵中“左下角”的值，即 dp[i+1][j-1]。
            // 当内层循环刚开始（j = i + 1）时，它的左下角对应的是无效区间（i+1 > j-1），长度为 0。
            int prev = 0;

            // 内层循环：从 i+1 开始往后遍历 j（作为区间的右端点）
            for (int j = i + 1; j < n; ++j) {

                // temp 记录的是当前未被修改的 dp[j]。
                // 注意：由于这一轮循环还没更新 dp[j]，所以它保存的其实还是上一轮（i+1行）留下来的旧值。
                // 也就是说，temp 等价于原二维的 dp[i+1][j]（即正下方邻居）。
                // 当内层循环步进到下一个 j 时，当前的这个“正下方邻居”就会变成下一个 j 的“左下角邻居”。
                int temp = dp[j];

                if (s[i] == s[j]) {
                    // 情况一：首尾字符相同
                    // 二维方程：dp[i][j] = dp[i+1][j-1] + 2;
                    // 一维对应：dp[i+1][j-1] 就是保存在 prev 中的“左下角”旧值
                    dp[j] = prev + 2;
                } else {
                    // 情况二：首尾字符不同
                    // 二维方程：dp[i][j] = max(dp[i+1][j], dp[i][j-1]);
                    // 一维对应：
                    // 1. 此时的 dp[j] 还没被改动，依然代表上一行（i+1行）的值，即 dp[i+1][j]（正下方）
                    // 2. 此时的 dp[j-1] 在当前轮次已经被更新过了，代表当前行（i行）的值，即 dp[i][j-1]（左边）
                    dp[j] = std::max(dp[j], dp[j - 1]);
                }

                // 核心交接：把刚刚暂存的旧 dp[j]（即上一行的值）赋给 prev。
                // 这样在下一次循环（j 变成 j+1）时，这个值恰好就成了新 j 的 dp[i+1][j-1]（左下角）。
                prev = temp;
            }
        }

        // 遍历结束后，i = 0，一维数组最后的 dp[n-1] 存储的就是区间 [0, n-1] 的最终答案
        return dp[n - 1];
    }
};
